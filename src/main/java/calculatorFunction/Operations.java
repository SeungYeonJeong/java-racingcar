package calculatorFunction;

public class Operations {

    double firstOperand, secondOperand;

    public double add() {
        return firstOperand + secondOperand;
    }

    public double sub() {
        return firstOperand - secondOperand;
    }

    public double mul() {
        return firstOperand * secondOperand;
    }

    public double div() {
        return firstOperand / secondOperand;
    }
}
